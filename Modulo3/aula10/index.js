//ARAYS

//Quando há necessidade de listar algo, para representar isso, é preciso usar array
// para representar isso deve-se usar os "  []  "

const listaCompras =  ["agua", "carne", "pao", "fruta"]

console.log(listaCompras)

//LEMBRANDO QUE O PRIMEIRO ITEM DA LISTA É 0
console.log(listaCompras[0])

//PARA ACESSAR O TERCEIRO ITEM DA LISTA DEVE SER ASSIM 
console.log(listaCompras[2])

//para accessar o ultimo item da ARRAY
console.log(listaCompras[4-1])

//para saber o comprimento da ARRAY
console.log(listaCompras.length)

//pode-se fazer isso::
console.log(listaCompras[listaCompras.length-2])

